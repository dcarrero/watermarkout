let currentLang = 'en';

/**
 * Obtiene el texto traducido para una clave.
 * Soporta placeholders: {pct}, {w}, {h}, {max}
 */
export function t(key, params = {}) {
  const text = translations[currentLang]?.[key] || translations.en[key] || key;
  return text.replace(/\{(\w+)\}/g, (_, k) => params[k] ?? `{${k}}`);
}

/** Retorna el idioma actual */
export function getLang() {
  return currentLang;
}

/** Retorna la lista de idiomas disponibles */
export function getLanguages() {
  return Object.entries(translations).map(([code, t]) => ({
    code,
    name: t.lang,
    dir: t.dir || 'ltr',
  }));
}

/** Cambia el idioma y actualiza el DOM */
export function setLang(lang) {
  if (!translations[lang]) return;
  currentLang = lang;
  localStorage.setItem('watermarkout-lang', lang);

  // Dirección del texto (RTL para árabe)
  const dir = translations[lang].dir || 'ltr';
  document.documentElement.dir = dir;
  document.documentElement.lang = lang;

  // Actualizar todos los elementos con data-i18n
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.dataset.i18n;
    const text = t(key);

    if (el.tagName === 'INPUT' && el.type === 'text') {
      el.placeholder = text;
    } else if (el.dataset.i18nAttr) {
      el.setAttribute(el.dataset.i18nAttr, text);
    } else {
      el.textContent = text;
    }
  });

  // Actualizar selector de idioma
  const select = document.getElementById('lang-select');
  if (select) select.value = lang;
}

/** Inicializa con el idioma guardado o del navegador */
export function initI18n() {
  const saved = localStorage.getItem('watermarkout-lang');
  const browserLang = navigator.language?.slice(0, 2);
  const lang = saved || (translations[browserLang] ? browserLang : 'en');
  setLang(lang);
}
